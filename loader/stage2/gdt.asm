;**************************************************************************************************
;
;		ReaverOS
;		gdt.asm
;		GDT routines
;
;**************************************************************************************************
bits	16

;**************************************************************************************************
;
;		install_gdt()
;
;**************************************************************************************************
install_gdt:
	cli
	pusha
	
	lgdt	[gdt]
	
	popa
	sti
	ret
	
;**************************************************************************************************
;
;		GDTs
;
;**************************************************************************************************
gdt_start:
	dd 0
	dd 0
	
; code:
	dw 0x0ffff
	dw 0
	db 0
	db 10011010b
	db 11001111b
	db 0
	
; data:
	dw 0x0ffff
	dw 0
	db 0
	db 10010010b
	db 11001111b
	db 0

gdt_end:

gdt:
	dw gdt_end - gdt_start - 1
	dd gdt_start